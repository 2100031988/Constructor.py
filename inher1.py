class Person:
    def __init__(self, Name, Age, Gender, Occupation):
        self.Name=Name
        self.Age=Age
        self.Gender=Gender
        self.Occupation=Occupation

        def display(self):
            print("Name : %s \nAge: %d \nGender: ")


class Student:
    def __init__(self, Name, Id, Hostel):
        self.Name=Name
        self.Id=Id
        self.Hostel=Hostel

        def display(self):
            print(self.Name, self.Id, self.Hostel)



